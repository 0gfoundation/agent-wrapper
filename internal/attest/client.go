// Signature format constants
const (
	// SignatureHexLength is the expected length of a hex-encoded signature
	// Format: "0x" + 65 bytes * 2 hex chars = 132 chars
	SignatureHexLength = 132
	// SignatureBytesLength is the length of a raw signature in bytes
	SignatureBytesLength = 65
	// SignaturePrefix is the standard prefix for hex-encoded signatures
	SignaturePrefix = "0x"
)

// Unseal performs the unseal operation to get the agentSeal private key
func (c *Client) Unseal(sealID, pubKey, imageHash string) (*UnsealResponse, error) {
	// 1. Create timestamp
	ts := time.Now().Unix()

	// 2. Create signature: sign(keccak256("ImageAttestation:"+sealID+":"+pubKey+":"+imageHash+":"+ts))
	signature, err := c.createUnsealSignature(sealID, pubKey, imageHash, ts)
	if err != nil {
		return nil, fmt.Errorf("failed to create signature: %w", err)
	}

	// 3. Build request
	req := &UnsealRequest{
		SealID:    sealID,
		PubKey:    pubKey,
		ImageHash: imageHash,
		Signature: signature,
		Timestamp: ts,
	}

	// 4. Send to Attestor
	resp, err := c.sendUnsealRequest(req)
	if err != nil {
		return nil, fmt.Errorf("unseal request failed: %w", err)
	}

	return resp, nil
}

// createUnsealSignature creates signature for unseal request
// Format: keccak256("ImageAttestation:"+sealID+":"+pubkey+":"+imageHash+":"+ts)
// Then sign with TEE private key (secp256k1), V normalized to 27/28
func (c *Client) createUnsealSignature(sealID, pubKey, imageHash string, ts int64) (string, error) {
	if c.teePrivKey == nil {
		return "", fmt.Errorf("TEE private key not set")
	}

	// Create message: "ImageAttestation:"+sealID+":"+pubkey+":"+imageHash+":"+ts
	message := fmt.Sprintf("ImageAttestation:%s:%s:%s:%d", sealID, pubKey, imageHash, ts)

	// Keccak256 hash
	hash := ethcrypto.Keccak256([]byte(message))

	// Sign with TEE private key (secp256k1)
	// go-ethereum's Sign returns a 65-byte signature with V already normalized to 27/28
	signature, err := ethcrypto.Sign(hash, c.teePrivKey)
	if err != nil {
		return "", fmt.Errorf("signing failed: %w", err)
	}

	// Encode signature: 0x + 65 bytes
	return "0x" + hex.EncodeToString(signature), nil
}

// sendUnsealRequest sends HTTP POST to /v1/unseal
func (c *Client) sendUnsealRequest(req *UnsealRequest) (*UnsealResponse, error) {
	// Marshal body
	jsonData, err := json.Marshal(req)
	if err != nil {
		return nil, err
	}

	// Create request
	url := c.baseURL + "/v1/unseal"
	httpReq, err := http.NewRequest("POST", url, bytes.NewReader(jsonData))
	if err != nil {
		return nil, err
	}

	// Set headers
	httpReq.Header.Set("Content-Type", "application/json")

	// Send request
	resp, err := c.httpClient.Do(httpReq)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()

	// Read body
	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}

	// Check status
	if resp.StatusCode != http.StatusOK {
		// Try to parse error response
		var errResp ErrorResponse
		if json.Unmarshal(body, &errResp) == nil && errResp.Code != "" {
			return nil, fmt.Errorf("attestor error: %s - %s", errResp.Code, errResp.Message)
		}
		return nil, fmt.Errorf("attestor returned status %d: %s", resp.StatusCode, string(body))
	}

	// Parse response
	var unsealResp UnsealResponse
	if err := json.Unmarshal(body, &unsealResp); err != nil {
		return nil, err
	}

	return &unsealResp, nil
}

// DecryptAgentSealKey decrypts the agentSeal key from the unseal response
// The scheme is "ecies-secp256k1-aes256gcm" - ECIES encryption with secp256k1 and AES-256-GCM
func (c *Client) DecryptAgentSealKey(encryptedKey string, TEEPrivateKey *ecdsa.PrivateKey) ([]byte, error) {
	// Strip 0x prefix if present
	hexStr := encryptedKey
	if len(hexStr) >= 2 && hexStr[0:2] == "0x" {
		hexStr = hexStr[2:]
	}

	// Parse encrypted key
	cipherText, err := hex.DecodeString(hexStr)
	if err != nil {
		return nil, fmt.Errorf("invalid hex: %w", err)
	}

	// Convert ECDSA private key to ECIES private key for secp256k1
	eciesPrivKey := ethecies.ImportECDSA(TEEPrivateKey)

	// Decrypt using ECIES with secp256k1
	// The encrypted data format: ephemeral public key + encrypted data + MAC
	plaintext, err := eciesPrivKey.Decrypt(cipherText, nil, nil)
	if err != nil {
		return nil, fmt.Errorf("ECIES decryption failed: %w", err)
	}

	return plaintext, nil
}

// DecryptSealedKey decrypts a sealed key using ECIES with secp256k1
// This is used to decrypt sealedKey → dataKey for intelligent data access
// The agentSealPriv must be a secp256k1 private key
func DecryptSealedKey(sealedKey []byte, agentSealPriv *ecdsa.PrivateKey) ([]byte, error) {
	if len(sealedKey) == 0 {
		return nil, errors.New("sealed key is empty")
	}
	if agentSealPriv == nil {
		return nil, errors.New("agent seal private key is nil")
	}

	// Convert ECDSA private key to ECIES private key for secp256k1
	eciesPrivKey := ethecies.ImportECDSA(agentSealPriv)

	// Decrypt using ECIES
	plaintext, err := eciesPrivKey.Decrypt(sealedKey, nil, nil)
	if err != nil {
		return nil, fmt.Errorf("ECIES decrypt sealed key failed: %w", err)
	}

	return plaintext, nil
}

// BytesToSecp256k1PrivateKey converts a 32-byte private key to secp256k1 ECDSA private key
// This is used to convert agentSealKey bytes to ECDSA for signing/decryption
func BytesToSecp256k1PrivateKey(keyBytes []byte) (*ecdsa.PrivateKey, error) {
	if len(keyBytes) != 32 {
		return nil, fmt.Errorf("invalid key length: expected 32 bytes, got %d", len(keyBytes))
	}

	// Use go-ethereum's ToECDSA which expects secp256k1
	privKey, err := ethcrypto.ToECDSA(keyBytes)
	if err != nil {
		return nil, fmt.Errorf("failed to parse secp256k1 private key: %w", err)
	}

	return privKey, nil
}

// Provision performs the provision operation to get the agentSeal private key
// Uses the /provision endpoint (current API)
func (c *Client) Provision(sealID, containerPubkey, imageHash string) (*ProvisionResponse, error) {
	// 1. Create timestamp
	ts := time.Now().Unix()

	// 2. Create signature: sign(keccak256("ImageAttestation:"+sealID+":"+pubkey+":"+imageHash+":"+ts))
	signature, err := c.createUnsealSignature(sealID, containerPubkey, imageHash, ts)
	if err != nil {
		return nil, fmt.Errorf("failed to create signature: %w", err)
	}

	// 3. Build request
	req := &ProvisionRequest{
		SealID:           "0x" + sealID,
		ContainerPubkey:  containerPubkey,
		ImageHash:        "0x" + strings.TrimPrefix(imageHash, "sha256:"),
		IssuedAt:         ts,
		SandboxSignature: signature,
	}

	// 4. Send to Attestor
	resp, err := c.sendProvisionRequest(req)
	if err != nil {
		return nil, fmt.Errorf("provision request failed: %w", err)
	}

	return resp, nil
}

