// StatusReportResponse is the response from /status
type StatusReportResponse struct {
	Accepted bool   `json:"accepted"`
	Message  string `json:"message,omitempty"`
}

// ReportStatus sends a status report to the Attestor
// The message format is: "StatusReport:<seal_id_0x>:<status>:<error_detail>"
// Signed with agent_seal_priv using Keccak256 + secp256k1, V normalized to 27/28
func (c *Client) ReportStatus(agentSealPriv *ecdsa.PrivateKey, sealID, status, errorDetail string) (*StatusReportResponse, error) {
	// 1. Create canonical message
	msg := fmt.Sprintf("StatusReport:0x%s:%s:%s", sealID, status, errorDetail)

	// 2. Keccak256 hash
	hash := ethcrypto.Keccak256([]byte(msg))

	// 3. Sign with agent_seal_priv (secp256k1)
	signature, err := ethcrypto.Sign(hash, agentSealPriv)
	if err != nil {
		return nil, fmt.Errorf("signing failed: %w", err)
	}

	// 4. Normalize V to 27/28 (crypto.Sign returns 0/1)
	signature[64] += 27

	// 5. Build request
	req := &StatusReportRequest{
		SealID:            "0x" + sealID,
		Status:            status,
		ErrorDetail:       errorDetail,
		AgentSealSignature: "0x" + hex.EncodeToString(signature),
	}

	// 6. Send to Attestor
	return c.sendStatusReportRequest(req)
}

