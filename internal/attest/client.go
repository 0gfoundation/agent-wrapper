// Client is the attestation service client
type Client struct {
	baseURL    string
	httpClient *http.Client
	teePrivKey *ecdsa.PrivateKey // TEE private key for signing
}

// Config holds client configuration
type Config struct {
	BaseURL string
	Timeout time.Duration
}

// NewClient creates a new attestation client
func NewClient(cfg *Config) *Client {
	timeout := cfg.Timeout
	if timeout == 0 {
		timeout = 30 * time.Second
	}

	return &Client{
		baseURL: cfg.BaseURL,
		httpClient: &http.Client{
			Timeout: timeout,
			Transport: &http.Transport{
				Proxy: nil,
			},
		},
	}
}

// SetTEEPrivateKey sets the TEE private key used for signing requests
// This is the key pair generated by the wrapper (secp256k1)
func (c *Client) SetTEEPrivateKey(privKey *ecdsa.PrivateKey) {
	c.teePrivKey = privKey
}

// SetTEEPrivateKeyFromHex sets the TEE private key from hex string
func (c *Client) SetTEEPrivateKeyFromHex(privKeyHex string) error {
	// Strip 0x prefix
	hexStr := privKeyHex
	if len(hexStr) >= 2 && hexStr[0:2] == "0x" {
		hexStr = hexStr[2:]
	}

	// Decode hex
	keyBytes, err := hex.DecodeString(hexStr)
	if err != nil {
		return fmt.Errorf("invalid hex key: %w", err)
	}

	// Use go-ethereum's ToECDSA to create secp256k1 private key
	privKey, err := ethcrypto.ToECDSA(keyBytes)
	if err != nil {
		return fmt.Errorf("failed to parse secp256k1 private key: %w", err)
	}

	c.teePrivKey = privKey
	return nil
}

