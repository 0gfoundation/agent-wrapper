// GetAgentSealKey performs full provision flow and returns the decrypted key
// This is the main entry point for getting the agentSeal private key
func (c *Client) GetAgentSealKey(sealID, pubKey, imageHash string) ([]byte, error) {
	// 1. Perform provision (current API)
	resp, err := c.Provision(sealID, pubKey, imageHash)
	if err != nil {
		return nil, err
	}

	// 2. Decrypt agentSeal key using ECIES
	key, err := c.DecryptAgentSealKey(resp.EncryptedAgentSealPriv, c.teePrivKey)
	if err != nil {
		return nil, fmt.Errorf("failed to decrypt agentSeal key: %w", err)
	}

	return key, nil
}

// SetTEEKeyFromHex is a convenience method to set the TEE key from hex
// This should be called before Unseal()
